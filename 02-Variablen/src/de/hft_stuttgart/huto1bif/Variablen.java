package de.hft_stuttgart.huto1bif;

public class Variablen {

  public static void main(String[] args) {
    byte meinByte = 0;
    short meinShort = 0;
    int meinInt = 0;
    long meinLong = 0;
    float meinFloat = 0.0f;
    double meinDouble = 0.0;
    char meinChar = 'a';
    boolean meinBool = true;

    System.out.println(meinByte);
    System.out.println(meinShort);
    System.out.println(meinInt);
    System.out.println(meinLong);
    System.out.println(meinFloat);
    System.out.println(meinDouble);
    System.out.println(meinChar);
    System.out.println(meinBool);
  }
}
