package de.hft_stuttgart.huto1bif;

public class BoolescheWerte {

  public static void main(String[] args) {
    boolean a = (5 < 7);
    System.out.println("5 < 7 liefert den booleschen Wert: " + a);

    boolean b = (5 > 7);
    System.out.println("5 > 7 liefert den booleschen Wert: " + b);

    boolean c = (5 <= 7);
    System.out.println("5 <= 7 liefert den booleschen Wert:" + c);

    boolean d = (5 >= 7);
    System.out.println("5 >= 7 liefert den booleschen Wert:" + d);

    boolean e = (5 != 7);
    System.out.println("5 != 7 liefert den booleschen Wert:" + e);

    boolean f = (5 == 7);
    System.out.println("5 == 7 liefert den booleschen Wert:" + f);
  }
}
